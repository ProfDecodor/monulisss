export class Me {

    constructor() {
        this.permat = "";
        this.firstName = "";
        this.lastName = "";
    }

    getPermat() {
        return this.permat;
    }

    setPermat(permat) {
        this.permat = permat;
    }

    getFirstName() {
        return this.firstName;
    }

    setFirstName(firstName) {
        this.firstName = firstName;
    }

    getLastName() {
        return this.lastName;
    }

    setLastName(lastName) {
        this.lastName = lastName;
    }
    
}
  